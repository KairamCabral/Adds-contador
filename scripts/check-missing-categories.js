/**
 * Script para diagnosticar produtos sem categoria
 * Mostra quais produtos t√™m categoria e quais est√£o como N/D
 */

const { PrismaClient } = require('@prisma/client');
const prisma = new PrismaClient();

async function check() {
  console.log('\n‚ïî‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïó');
  console.log('‚ïë  DIAGN√ìSTICO DE CATEGORIAS - PRODUTOS                   ‚ïë');
  console.log('‚ïö‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïù\n');

  try {
    // Buscar produtos √∫nicos com N/D
    const produtosNd = await prisma.$queryRaw`
      SELECT DISTINCT "Produto" as produto, COUNT(*) as ocorrencias
      FROM vw_vendas
      WHERE "Categoria" = 'N/D'
      GROUP BY "Produto"
      ORDER BY ocorrencias DESC
    `;

    // Buscar produtos √∫nicos com categoria
    const produtosCat = await prisma.$queryRaw`
      SELECT DISTINCT "Produto" as produto, "Categoria" as categoria, COUNT(*) as ocorrencias
      FROM vw_vendas
      WHERE "Categoria" != 'N/D'
      GROUP BY "Produto", "Categoria"
      ORDER BY "Categoria", ocorrencias DESC
    `;

    console.log('‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê');
    console.log('‚ùå PRODUTOS SEM CATEGORIA (N/D):');
    console.log('‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê\n');
    
    if (produtosNd.length === 0) {
      console.log('   ‚úÖ Nenhum produto sem categoria!\n');
    } else {
      produtosNd.forEach((p, idx) => {
        console.log(`   ${idx + 1}. ${p.produto}`);
        console.log(`      Ocorr√™ncias: ${p.ocorrencias}x\n`);
      });
    }

    console.log('‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê');
    console.log('‚úÖ PRODUTOS COM CATEGORIA:');
    console.log('‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê\n');
    
    if (produtosCat.length === 0) {
      console.log('   ‚ö†Ô∏è  Nenhum produto com categoria encontrado\n');
    } else {
      let currentCat = '';
      produtosCat.forEach((p) => {
        if (p.categoria !== currentCat) {
          currentCat = p.categoria;
          console.log(`\n   üìÇ ${currentCat}:`);
        }
        console.log(`      ‚Ä¢ ${p.produto} (${p.ocorrencias}x)`);
      });
      console.log('');
    }

    console.log('‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê');
    console.log('üìä ESTAT√çSTICAS:');
    console.log('‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê\n');

    const total = await prisma.vwVendas.count();
    const semCategoria = await prisma.vwVendas.count({
      where: { categoria: 'N/D' }
    });
    const comCategoria = total - semCategoria;

    const percentComCategoria = total > 0 ? ((comCategoria / total) * 100).toFixed(1) : 0;
    const percentSemCategoria = total > 0 ? ((semCategoria / total) * 100).toFixed(1) : 0;

    console.log(`   Total de registros: ${total}`);
    console.log(`   Com categoria: ${comCategoria} (${percentComCategoria}%)`);
    console.log(`   Sem categoria: ${semCategoria} (${percentSemCategoria}%)`);
    console.log('');

    console.log(`   Produtos √∫nicos sem categoria: ${produtosNd.length}`);
    console.log(`   Produtos √∫nicos com categoria: ${produtosCat.length}`);
    console.log('');

    if (semCategoria > 0) {
      console.log('üí° RECOMENDA√á√ÉO:');
      console.log('   Execute um novo sync para tentar enriquecer os produtos faltantes:');
      console.log('   ‚Üí Acesse a UI e clique em "Sincronizar agora"');
      console.log('   ‚Üí Ou use: POST /api/admin/sync\n');
    } else {
      console.log('‚ú® Todas as vendas t√™m categoria! Sistema funcionando perfeitamente.\n');
    }

  } catch (error) {
    console.error('‚ùå Erro:', error.message);
    process.exit(1);
  } finally {
    await prisma.$disconnect();
  }
}

check();
